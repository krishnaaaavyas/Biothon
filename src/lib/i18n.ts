import { useLanguageContext } from "@/contexts/language-context";

const reportedMissingKeys = new Set<string>();

export function t(key: string, lang?: Lang, params?: Record<string, any>): string {
  let activeLang: Lang = "en";
  try {
    const raw = localStorage.getItem("hg.lang.v1");
    if (raw) {
      try {
        const parsed = JSON.parse(raw);
        if (["en", "hi", "gu", "mr", "bn", "ta", "te", "kn"].includes(parsed)) {
          activeLang = parsed as Lang;
        }
      } catch {
        if (["en", "hi", "gu", "mr", "bn", "ta", "te", "kn"].includes(raw)) {
          activeLang = raw as Lang;
        }
      }
    }
  } catch {
    // Ignore localStorage error
  }

  const currentLang = lang || activeLang;
  const entry = (translations as any)[key];

  if (!entry) {
    if (process.env.NODE_ENV === "development" && !reportedMissingKeys.has(key)) {
      reportedMissingKeys.add(key);
      console.warn(`[i18n] Missing translation key: "${key}"`);
    }
    return key;
  }

  let text = entry[currentLang] || entry.en || key;

  if (params && typeof params === "object") {
    Object.keys(params).forEach((paramKey) => {
      const val = String(params[paramKey]);
      text = text.replace(new RegExp(`\\{\\{${paramKey}\\}\\}`, "g"), val);
    });
  }

  return text;
}

export const tr = t;

export function useLanguage(): Lang {
  try {
    const context = useLanguageContext();
    return context.language;
  } catch {
    const raw = localStorage.getItem("hg.lang.v1");
    if (!raw) return "en";
    try {
      const parsed = JSON.parse(raw);
      if (["en", "hi", "gu", "mr", "bn", "ta", "te", "kn"].includes(parsed)) {
        return parsed as Lang;
      }
    } catch {
      if (["en", "hi", "gu", "mr", "bn", "ta", "te", "kn"].includes(raw)) {
        return raw as Lang;
      }
    }
    return "en";
  }
}
